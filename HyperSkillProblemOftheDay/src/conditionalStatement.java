



public class conditionalStatement {
	public static void main(String[] args) {
		int x1 = 1;
		int x2 = -100;
		int y1 = -100;
		int y2 = 1;
		if((x1 == x2) || (y1 == y2) || Math.abs(x1 - x2) == Math.abs(y1 - y2)){
            System.out.println("YES"); 
            
        }else{ 
            System.out.println("NO");
    }
}
}


